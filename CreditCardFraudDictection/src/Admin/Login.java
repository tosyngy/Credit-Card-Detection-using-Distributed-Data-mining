    void userLogin() {
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Type your Username", "Credit Card Admin", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
            return;
        }
        if (jPasswordField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please type in Password", "Credit Card Admin", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.requestFocus();
            return;
        }

        //testing if internet connection is available

        try {
            String sqlQuery = "select username from admin where username='" + jTextField1.getText() + "'";
            db.rs = db.st.executeQuery(sqlQuery);
            if (db.rs.next()) {
                if (db.rs.getString(1).equals(jPasswordField1.getText())) {
                    this.setVisible(false);
                    new Register().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Invalid Password");
                    b++;
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Invalid Username and Password");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "There was a Problem retrying this data");
            e.printStackTrace();
        }

    }
